export const templateItems = baseTemplates.map((template) => ({
  ...template,
  platformSpecs: template.platforms.map((platform) => platformSpecs[platform] ? { platform, ...platformSpecs[platform] } : null).filter(Boolean),
  channelStrategy: template.platforms.map((platform) => [platform, channelStrategies[platform] ?? []]),
  editorControls: editorControls[template.editorMode] ?? editorControls.text,
  productionFlow: productionFlows[template.editorMode] ?? productionFlows.text,
  layoutSlots: layoutSlots[template.editorMode] ?? layoutSlots.text,
  templateBlueprint: {
    ...(contentFormatBlueprints[template.editorMode] ?? contentFormatBlueprints.text),
    cardBlueprints: cardBlueprints[template.editorMode] ?? cardBlueprints.text,
    platformExport: template.platforms.map((platform) => ({
      platform,
      placements: platformSpecs[platform]?.placements ?? [],
      ratios: platformSpecs[platform]?.ratios ?? [],
      exportCheck: platformSpecs[platform]?.exportCheck ?? []
    }))
  }
}));

export function findTemplateItem(templateId) {
  if (!templateId) return null;
  return templateItems.find((item) => item.id === templateId) ?? null;
}

export function resolveTemplateItem(template) {
  if (!template?.id) return null;
  const catalogItem = findTemplateItem(template.id);
  return catalogItem ? { ...catalogItem, ...template } : template;
}
